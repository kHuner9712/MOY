const CACHE_NAME = "moy-mobile-shell-v1";
const CORE_ASSETS = [
  "/",
  "/today",
  "/capture",
  "/briefings",
  "/manifest.webmanifest",
  "/icons/moy-icon.svg",
  "/icons/moy-icon-maskable.svg"
];

self.addEventListener("install", (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => cache.addAll(CORE_ASSETS)).catch(() => null)
  );
  self.skipWaiting();
});

self.addEventListener("activate", (event) => {
  event.waitUntil(
    caches.keys().then((keys) =>
      Promise.all(
        keys
          .filter((key) => key !== CACHE_NAME)
          .map((key) => caches.delete(key))
      )
    )
  );
  self.clients.claim();
});

self.addEventListener("fetch", (event) => {
  const request = event.request;
  if (request.method !== "GET") return;
  const url = new URL(request.url);
  if (url.origin !== self.location.origin) return;

  const isPriorityPage =
    url.pathname === "/today" ||
    url.pathname === "/capture" ||
    url.pathname === "/briefings" ||
    url.pathname.startsWith("/deals/") ||
    url.pathname.startsWith("/customers/");

  if (!isPriorityPage) return;

  event.respondWith(
    caches.match(request).then((cached) => {
      const networkFetch = fetch(request)
        .then((response) => {
          if (response && response.status === 200) {
            const copy = response.clone();
            caches.open(CACHE_NAME).then((cache) => cache.put(request, copy));
          }
          return response;
        })
        .catch(() => cached);

      return cached || networkFetch;
    })
  );
});
